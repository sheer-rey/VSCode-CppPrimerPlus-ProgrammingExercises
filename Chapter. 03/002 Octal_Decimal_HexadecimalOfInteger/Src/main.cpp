#include <iostream>
using namespace std;

int main()
{
    int v_oct = 052;
    int v_dec = 52;
    int v_hex = 0x52;

    cout << "Different Values of 52 in Octal, Decimal and Hexadecimal." << endl;
    cout << "Octal Value " << oct << v_oct << " in Decimal is: " << dec << v_oct << endl;
    cout << "Decimal Value " << dec << v_dec << " in Decimal is: " << dec << v_dec << endl;
    cout << "Hexadecimal Value " << hex << v_hex << " in Decimal is: " << dec << v_hex << endl;

    return 0;
}